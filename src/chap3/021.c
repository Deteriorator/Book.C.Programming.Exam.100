/*
 * 4位反序数
 *
 */

#include <stdio.h>

int main() {
    int i;
    for (i = 1002; i < 1111; i++)
        if (i % 10 * 1000 + i / 10 % 10 * 100 + i / 100 % 10 * 10 + i / 1000 == i * 9)
            printf("The number satisfied states condition is: %d\n", i);
}
