/*
 *  在屏幕中用“*”画一个空心的圆
 */

#include <stdio.h>
#include <math.h>

void main() {
    double y;
    int x, m;
    for (y = 10; y >= -10; y--) {
        m = 2.9 * sqrt(100 - y * y);
        for (x = 1; x < 30 - m; x++)
            printf(" ");
        printf("*");
        for (; x < 30 + m; x++)
            printf(" ");
        printf("*\n");
    }
}

